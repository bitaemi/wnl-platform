Zdajemy sobie sprawę, że na stronie jest co czytać o kursie. :)

Wciąż jednak mamy Wam bardzo wiele do powiedzenia! Tym razem jednak, postawiliśmy na bardziej interaktywną formę przekazu.

Dlatego serdecznie zapraszamy Was do wzięcia udziału w streamingu, podczas którego będziemy odpowiadać na wszystkie Wasze pytania. Na pewno dokładnie opowiemy o warsztatach z naszymi wyjątkowymi gośćmi, metodologii nauczania oraz o tym, jak wyobrażamy sobie codzienną interakcję z platformą e-learningową. Nie zabraknie też rozważań o naszych dalszych planach oraz misji "Więcej niż LEK". :)

Zobacz wydarzenie na facebooku (https://www.facebook.com/events/1818711421723449/)

Uruchomiliśmy płatność rozłożoną w czasie

Jedno z Waszych najczęstszych pytań dotyczyło rozłożenia płatności w czasie. Od dziś jest już taka możliwość! Niezależnie od wybranego wariantu i całościowej kwoty, płatność rozkładana jest na 3 części:

1. Pierwsza płatna do 7 dni od złożenia zamówienia
2. Druga płatna do 15 czerwca 2017r.
3. Trzecia płatna do 15 lipca 2017r.

Dokładne kwoty znajdziecie na stronie Cennik (https://wiecejnizlek.pl/cennik).

Mamy też odpowiedzi na dodatkowe pytania, które padły w kontekście nowej formy płatności:

Jak można skorzystać z nowego sposobu płatności?
Opcja ta dostępna jest w ostatnim kroku - "Potwierdzenie zamówienia" - podczas wyboru metody płatności. Dokładną instrukcję również znajdziecie w Cenniku (https://wiecejnizlek.pl/cennik).

Czy można zmienić metodę płatności?
Jak najbardziej! Jeżeli Twoje zamówienie nie zostało jeszcze opłacone, możesz zmienić metodę płatności poprzez stronę Twoje zamówienia (https://platforma.wiecejnizlek.pl/app/myself/orders).

